/** 運営の有人窓口（FAQで解決しないとき案内する）。 */
export const SUPPORT_CONTACT = "support@fortetomi.jp";

export interface SupportAnswer {
  /** FAQと一致したか。false のときは有人窓口へ案内。 */
  matched: boolean;
  text: string;
  /** 参考として併記する関連FAQ。 */
  related: Faq[];
}

/**
 * ユーザーの質問文に最もマッチするFAQを返す簡易マッチャ。
 * キーワードの含有数でスコアリングし、最上位を回答とする。
 */
export function answerQuestion(input: string): SupportAnswer {
  const text = input.trim().toLowerCase();
  if (!text) {
    return { matched: false, text: "ご質問を入力してください。", related: [] };
  }

  const scored = FAQS.map((faq) => {
    let score = 0;
    for (const kw of faq.keywords) {
      if (text.includes(kw.toLowerCase())) score += 2;
    }
    // 質問文そのものとの部分一致も弱く加点。
    if (faq.q.toLowerCase().split(/[、。・\s]/).some((w) => w && text.includes(w))) {
      score += 1;
    }
    return { faq, score };
  })
    .filter((s) => s.score > 0)
    .sort((a, b) => b.score - a.score);

  if (scored.length === 0) {
    return {
      matched: false,
      text: `申し訳ありません、その内容はFAQに見つかりませんでした。お手数ですが運営（${SUPPORT_CONTACT}）までお問い合わせください。下のよくある質問もご参照ください。`,
      related: FAQS.slice(0, 3),
    };
  }

  const best = scored[0].faq;
  const related = scored.slice(1, 3).map((s) => s.faq);
  return { matched: true, text: best.a, related };
}
